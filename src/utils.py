# 天気コード → 絵文字マッピング
WEATHER_EMOJI_MAP = {
    "1": "☀️",   # 晴ベース
    "2": "☁️",   # 曇ベース
    "3": "🌧️",  # 雨ベース
    "4": "🌨️",  # 雪ベース
}


def weather_code_to_text(code: str) -> str:
    """天気コードを説明テキストに変換"""
    return WEATHER_CODE_MAP.get(str(code), f"不明({code})")


def weather_code_to_emoji(code: str) -> str:
    """天気コードを絵文字に変換"""
    code_str = str(code)
    if not code_str:
        return "❓"
    prefix = code_str[0]
    return WEATHER_EMOJI_MAP.get(prefix, "🌤️")


def now_jst() -> datetime:
    """現在のJST日時を返す"""
    return datetime.now(JST)


def parse_jst(iso_str: str) -> datetime:
    """ISO 8601文字列をJST datetimeに変換"""
    dt = datetime.fromisoformat(iso_str)
    return dt.astimezone(JST)


def format_date_jp(dt: datetime) -> str:
    """JST datetimeを日本語日付に変換（例: 3月26日(木)）"""
    weekdays = ["月", "火", "水", "木", "金", "土", "日"]
    wd = weekdays[dt.weekday()]
    return f"{dt.month}月{dt.day}日({wd})"


def format_time_jp(dt: datetime) -> str:
    """JST datetimeを時刻文字列に変換（例: 06:00）"""
    return dt.strftime("%H:%M")
